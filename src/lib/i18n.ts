export type Lang = 'zh' | 'en'

const LANG_KEY = 'tarot.lang.v1'

export function loadLang(): Lang {
  try {
    const v = localStorage.getItem(LANG_KEY)
    return v === 'en' ? 'en' : 'zh'
  } catch {
    return 'zh'
  }
}

export function saveLang(lang: Lang): void {
  try {
    localStorage.setItem(LANG_KEY, lang)
  } catch {
    // ignore
  }
}

